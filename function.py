def sum(num1,num2):
    return num1+num2

def multiply(num1,num2):
    return num1*num2

def average(num1,num2):
    return (num1+num2)/2

num1=5
num2=10
print("The sum of num1 and num2 is:",sum(num1,num2))
print("The multiply of num1 and num2 is:",multiply(num1,num2))
print("The average of num1 and num2 is:",average(num1,num2))
